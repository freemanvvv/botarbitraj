def search_sources_csv(query: str, sources_csv_path: str, limit: int = 5) -> list[dict]:
    """Ищет релевантные документы в sources.csv по ключевым словам."""
    import csv
    results = []
    keywords = query.lower().split()
    try:
        with open(sources_csv_path, newline="", encoding="utf-8") as f:
            for row in csv.DictReader(f):
                title = (row.get("title", "") or "").lower()
                number = (row.get("number", "") or "").lower()
                if any(kw in title or kw in number for kw in keywords):
                    results.append(row)
    except Exception:
        pass
    return results[:limit]
